package ver01;

public class UserVo {
	String u_name, u_id, u_pw;

	public UserVo(String u_name, String u_id, String u_pw) {
		super();
		this.u_name = u_name;
		this.u_id = u_id;
		this.u_pw = u_pw;
	}

	public String getU_name() {
		return u_name;
	}

	public String getU_id() {
		return u_id;
	}

	public String getU_pw() {
		return u_pw;
	}
	
	
	
}
